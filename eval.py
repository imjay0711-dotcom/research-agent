# STEP 2 — DEFINE EVALUATION METRICS
# ─────────────────────────────────────────────────────────────────────────────

def evaluate_report(report: str, query: str, must_contain: list) -> dict:
    """
    Evaluate the quality of a generated report using 4 metrics.

    Input:
        report       — the report text generated by the agent
        query        — the original research query
        must_contain — list of words that must appear in the report

    Output:
        dict with:
          - scores      : pass/fail for each metric (1 or 0)
          - total_score : percentage of metrics passed
          - feedback    : explanation of each score
    """

    scores = {}
    feedback = {}

    # ── Metric 1: Report Length ───────────────────────────────────────────────
    # A good report should have at least 200 words
    # Short reports mean the agent didn't gather enough information
    word_count = len(report.split())
    scores["report_length"] = 1 if word_count >= 200 else 0
    feedback["report_length"] = (
        f"PASS ({word_count} words)" if scores["report_length"]
        else f"FAIL (only {word_count} words, need 200+)"
    )

    # ── Metric 2: Has Required Sections ──────────────────────────────────────
    # A good report should have these 3 sections:
    # Summary, Key Findings, References
    # We check by looking for these words in the report (case-insensitive)
    report_lower = report.lower()
    required_sections = ["summary", "findings", "references"]
    sections_found = [s for s in required_sections if s in report_lower]
    scores["has_sections"] = 1 if len(sections_found) >= 2 else 0
    feedback["has_sections"] = (
        f"PASS (found: {sections_found})" if scores["has_sections"]
        else f"FAIL (only found: {sections_found}, need summary+findings+references)"
    )

    # ── Metric 3: Topic Relevance ─────────────────────────────────────────────
    # The report should mention words from the query
    # This checks if the agent stayed on topic
    query_words = [w.lower() for w in query.split() if len(w) > 3]
    # must_contain words must ALL appear in the report
    must_found = [w for w in must_contain if w.lower() in report_lower]
    scores["topic_relevant"] = 1 if len(must_found) == len(must_contain) else 0
    feedback["topic_relevant"] = (
        f"PASS (found key terms: {must_found})" if scores["topic_relevant"]
        else f"FAIL (missing terms: {[w for w in must_contain if w not in report_lower]})"
    )

    # ── Metric 4: Report Not Empty / No Error ────────────────────────────────
    # Check the report is real content, not an error message
    is_valid = (
        len(report) > 100 and           # more than 100 characters
        "error" not in report_lower[:50] and  # doesn't start with error
        "failed" not in report_lower[:50]     # doesn't start with failed
    )
    scores["valid_report"] = 1 if is_valid else 0
    feedback["valid_report"] = (
        "PASS (report looks valid)" if scores["valid_report"]
        else "FAIL (report seems to be an error or empty)"
    )

    # ── Calculate Total Score ─────────────────────────────────────────────────
    # total_score = percentage of metrics passed
    # Example: 3 out of 4 = 75%
    total = sum(scores.values())
    max_score = len(scores)
    percentage = (total / max_score) * 100

    return {
        "scores": scores,           # individual metric results
        "total_score": percentage,  # overall percentage
        "feedback": feedback,       # explanation of each result
        "word_count": word_count,   # total words in report
    }


# ─────────────────────────────────────────────────────────────────────────────
# STEP 3 — RUN A SINGLE TEST
# ─────────────────────────────────────────────────────────────────────────────

def run_single_test(test_case: dict) -> dict:
    """
    Run the agent on one test case and evaluate the result.

    Input:  test_case dict (id, query, must_contain, description)
    Output: result dict with query, report, scores, timing
    """

    print(f"\n{'─'*60}")
    print(f"TEST {test_case['id']}: {test_case['description']}")
    print(f"Query: {test_case['query']}")
    print(f"{'─'*60}")

    # Record start time
    # We measure how long the agent takes to complete each query
    start_time = time.time()

    try:
        # Run the agent!
        # This calls run_agent() from agent.py
        # The agent will search, fetch PDFs, query ChromaDB, generate report
        report = run_agent(test_case["query"])

        # Calculate how long it took
        elapsed = time.time() - start_time

        # Handle empty report
        if not report or len(report.strip()) == 0:
            report = "ERROR: Agent returned empty report"

        # Evaluate the report quality
        evaluation = evaluate_report(
            report=report,
            query=test_case["query"],
            must_contain=test_case["must_contain"]
        )

        # Print results to terminal
        print(f"\nResults for Test {test_case['id']}:")
        print(f"  Time taken  : {elapsed:.1f} seconds")
        print(f"  Word count  : {evaluation['word_count']}")
        print(f"  Total score : {evaluation['total_score']:.0f}%")
        print(f"\n  Metric breakdown:")
        for metric, fb in evaluation["feedback"].items():
            icon = "  ✅" if "PASS" in fb else "  ❌"
            print(f"{icon} {metric}: {fb}")

        # Return complete result
        return {
            "test_id": test_case["id"],
            "query": test_case["query"],
            "description": test_case["description"],
            "report": report,
            "evaluation": evaluation,
            "time_seconds": round(elapsed, 1),
            "status": "completed"
        }

    except Exception as e:
        # If agent crashes, record the error and continue
        elapsed = time.time() - start_time
        error_msg = str(e)
        print(f"\n  ERROR: Agent failed — {error_msg}")

        return {
            "test_id": test_case["id"],
            "query": test_case["query"],
            "description": test_case["description"],
            "report": f"FAILED: {error_msg}",
            "evaluation": {
                "scores": {"report_length": 0, "has_sections": 0,
                          "topic_relevant": 0, "valid_report": 0},
                "total_score": 0,
                "feedback": {"error": error_msg},
                "word_count": 0,
            },
            "time_seconds": round(elapsed, 1),
            "status": "failed"
        }


