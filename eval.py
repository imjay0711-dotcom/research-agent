# STEP 4 — RUN ALL TESTS
# ─────────────────────────────────────────────────────────────────────────────

def run_all_tests(test_cases: list = None, save_report: bool = True) -> dict:
    """
    Run all test cases and generate a summary report.

    Input:
        test_cases   — list of test cases (default: TEST_CASES)
        save_report  — whether to save results to JSON file
    Output:
        summary dict with all results and overall statistics
    """

    if test_cases is None:
        test_cases = TEST_CASES

    print("\n" + "=" * 60)
    print("  RESEARCH AGENT EVALUATION")
    print(f"  Running {len(test_cases)} test cases")
    print(f"  Started at: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60)

    # Check knowledge base before starting
    stats = get_stats()
    print(f"\nKnowledge base: {stats['total_papers']} papers, "
          f"{stats['total_chunks']} chunks")

    # Run all tests and collect results
    results = []
    for test_case in test_cases:
        result = run_single_test(test_case)
        results.append(result)

    # ── Calculate Overall Statistics ─────────────────────────────────────────
    total_tests = len(results)

    # Count completed (not failed) tests
    completed = [r for r in results if r["status"] == "completed"]

    # Average score across all completed tests
    avg_score = (
        sum(r["evaluation"]["total_score"] for r in completed) / len(completed)
        if completed else 0
    )

    # Count how many tests passed (score >= 75%)
    passed = [r for r in completed if r["evaluation"]["total_score"] >= 75]

    # Average time per test
    avg_time = (
        sum(r["time_seconds"] for r in results) / total_tests
        if results else 0
    )

    # Per-metric stats — how often did each metric pass?
    metric_stats = {}
    metrics = ["report_length", "has_sections", "topic_relevant", "valid_report"]
    for metric in metrics:
        passed_count = sum(
            1 for r in completed
            if r["evaluation"]["scores"].get(metric, 0) == 1
        )
        metric_stats[metric] = {
            "passed": passed_count,
            "total": len(completed),
            "rate": f"{(passed_count/len(completed)*100):.0f}%" if completed else "0%"
        }

    # Build summary
    summary = {
        "run_at": datetime.now().isoformat(),
        "total_tests": total_tests,
        "completed": len(completed),
        "failed": total_tests - len(completed),
        "passed_75_percent": len(passed),
        "average_score": round(avg_score, 1),
        "average_time_seconds": round(avg_time, 1),
        "metric_stats": metric_stats,
        "results": results,
    }

    # ── Print Summary ─────────────────────────────────────────────────────────
    print("\n" + "=" * 60)
    print("  EVALUATION SUMMARY")
    print("=" * 60)
    print(f"  Total tests run   : {total_tests}")
    print(f"  Completed         : {len(completed)}")
    print(f"  Failed            : {total_tests - len(completed)}")
    print(f"  Passed (≥75%)     : {len(passed)} / {total_tests}")
    print(f"  Average score     : {avg_score:.1f}%")
    print(f"  Average time      : {avg_time:.1f} seconds per query")
    print(f"\n  Metric pass rates:")
    for metric, stat in metric_stats.items():
        print(f"    {metric:20s}: {stat['rate']} ({stat['passed']}/{stat['total']})")

    # Overall grade
    print(f"\n  Overall Grade: ", end="")
    if avg_score >= 90:
        print("A — Excellent! 🏆")
    elif avg_score >= 75:
        print("B — Good! ✅")
    elif avg_score >= 60:
        print("C — Needs improvement 🔧")
    else:
        print("D — Needs significant work ❌")

    # ── Save to JSON ──────────────────────────────────────────────────────────
    if save_report:
        # Save to evals/ folder
        os.makedirs("evals", exist_ok=True)
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        report_path = os.path.join("evals", f"eval_report_{timestamp}.json")

        # Save summary without full reports (to keep file small)
        save_data = {k: v for k, v in summary.items() if k != "results"}
        save_data["results"] = [
            {k: v for k, v in r.items() if k != "report"}
            for r in results
        ]

        with open(report_path, "w") as f:
            json.dump(save_data, f, indent=2)

        print(f"\n  Report saved to: {report_path}")

    print("=" * 60)
    return summary


